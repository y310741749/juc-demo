package test.study5;

import cn.hutool.core.thread.ThreadUtil;
import lombok.extern.slf4j.Slf4j;

import java.sql.*;
import java.util.*;
import java.util.concurrent.Executor;
import java.util.concurrent.atomic.AtomicIntegerArray;

@Slf4j
public class Test5 {
    public static void main(String[] args) {
        Test5 test5 = new Test5(5);
        for (int i=0;i<10;i++){
            new Thread(() -> {
                Connection jie = test5.jie();
                log.info("拿到连接对象{}", jie);
                ThreadUtil.sleep(3000);
                test5.huan(jie);
                log.info("归还连接对象{}",jie);
            }).start();
        }


    }
    private final int poolSize;
    private Connection[] connections;
    private AtomicIntegerArray state;

    public Test5(int poolSize) {
        this.poolSize = poolSize;
        connections = new Connection[poolSize];
        state = new AtomicIntegerArray(new int[poolSize]);
        for (int i = 0; i < poolSize; i++) {
            connections[i] = new MyConnection();
        }
    }

    Connection jie() {
        while (true) {
            for (int i = 0; i < poolSize; i++) {
                if (state.get(i) == 0) {
                    state.compareAndSet(i, 0, 1);
                    return connections[i];
                }
            }
            synchronized (this) {
                try {
                    this.wait();
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
            }
        }
    }

    void huan(Connection connection) {
        for (int i = 0; i < poolSize; i++) {
            if (connections[i] == connection) {
                synchronized (this) {
                    state.set(i, 0);
                    this.notify();
                }
                return;

            }
        }
    }
}

